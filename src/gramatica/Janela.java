    private void verificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verificarActionPerformed
        // TODO add your handling code here:
        verificar.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                Gramatica gramatica = new Gramatica(estadosNaoTerminais.getText(), estadosTerminais.getText(), 
                        regrasDeProducao.getText(), simboloInicial.getText());
                
                ArrayList<String> regras = new ArrayList<String>();
                regras.addAll(Arrays.asList(regrasDeProducao.getText().split(", ")));
                String aux = "";
                for (int i = 0; i < regras.size(); i++)
                    aux += regras.get(i).substring(0, 1) + " -> " + regras.get(i).substring(1) + "\n";
                saida.setText(aux);
                if (gramatica.fazerVerificacao(entrada.getText()))
                {
                    ArrayList<String> caminhoPercorrido = new ArrayList<>();
                    String buffer = gramatica.getBuffer();
                    String bufferInv = "";
                    caminhoPercorrido.addAll(Arrays.asList(buffer.split("\n")));
                    for (int i = caminhoPercorrido.size() - 1; i >= 0; i--)
                        bufferInv += caminhoPercorrido.get(i) + "\n";
                    caminho.setBackground(Color.GREEN);
                    caminho.setText(bufferInv);
                }
                else
                {
                    caminho.setBackground(Color.red);
                    caminho.setText("");
                }
                try {
                
                } catch (HeadlessException e1) {
                    e1.printStackTrace();
                }
            }
	});
    }//GEN-LAST:event_verificarActionPerformed
